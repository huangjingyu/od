package huangjingyu.od.one;

public class Restaurant {
	private String name;

	private String address;

	private String telephone;

	private String cuisine;

	private String recentInspectionDate;

	private String grade;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getCuisine() {
		return cuisine;
	}

	public void setCuisine(String cuisine) {
		this.cuisine = cuisine;
	}

	public String getRecentInspectionDate() {
		return recentInspectionDate;
	}

	public void setRecentInspectionDate(String recentInspectionDate) {
		this.recentInspectionDate = recentInspectionDate;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}
}
